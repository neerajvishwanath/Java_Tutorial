package encapsulation;

public class Ex1_Encapsulation {

	
	 private int rollno;                                        
	    private String name;


	    //Getter Methods
	    public int getRollno() {
	        return rollno;
	    }

	    //Setter Methods
	    public void setRollno(int rollno) {
	    	this.rollno = rollno;                
	    }


	    public String getName() {
	        return name;
	    }


	    public void setName(String name) {
	        this.name = name;
	    }

	}

