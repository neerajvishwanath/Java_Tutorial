package flowcontrolstatements;

public class HandleBreakandContinue_For_If {
	 public static void main(String[] args) {


	        //Break with For Loop        
	        for(int i = 0; i<=10; i++)
	        {
	            if((i==6)) {
	                break;
	            }
	            System.out.print(i);
	        }


	        System.out.println();

	        //Continue with For Loop    
	        for(int i = 0; i<=10; i++) {
	            if(i==3) {
	                continue;
	            }
	            System.out.print(i);

	        }





	        }

}
